class MoneyCalculator:
    @staticmethod
    def add(amount1: float,  amount2: float) -> float:
        return amount1 + amount2
    
    @staticmethod
    def times(amount: float,  value: int) -> float:
        return amount * value
    
    @staticmethod
    def divide(amount: float,  value: int) -> float:
        return amount / value